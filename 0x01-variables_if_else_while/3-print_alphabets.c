#include <stdio.h>


/**
 *
 *  * main - Prints the alphabet.
 *
 *   *
 *
 *    * Return: Always 0 (Success)
 *
 *     */
int main(void)
{
	char henry = 'a';

	char hamza = 'A';
for (henry = 'a' ; henry <= 'z'; henry++)
{
	putchar(henry);
}
	for (hamza = 'A' ; hamza <= 'Z'; hamza++)
{
	putchar(hamza);
}
	putchar('\n');

return (0);
}
